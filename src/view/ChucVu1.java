    public void loadTB() {
        List<ChucVu> list = cvSV.getList();
        model = (DefaultTableModel) tblChucVu.getModel();
        model.setColumnCount(0);
        model.addColumn("Id");
        model.addColumn("Mã");
        model.addColumn("Tên Chức Vụ");

        model.setRowCount(0);
        for (ChucVu c : list) {
            model.addRow(new Object[]{
                c.getId(), c.getMa(), c.getTen()
            });
        }

    }

    public boolean checkMa() {
        List<ChucVu> list = cvSV.getList();
        for (ChucVu c : list) {
            if (c.getMa().equals(txtMa.getText())) {
                return false;
            }
        }
        return true;
    }

