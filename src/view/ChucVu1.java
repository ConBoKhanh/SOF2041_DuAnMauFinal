    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try {
            ChucVu c = new ChucVu();
            c.setId(lbId.getText());
            c.setMa(txtMa.getText());
            c.setTen(txtTen.getText());
            if (checkMa() == true) {
                cvSV.add(c);
                loadTB();
                JOptionPane.showMessageDialog(this, "Thêm thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Mã đã tồn tại!");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Thêm thất bại!");
        }

    }//GEN-LAST:event_btnThemActionPerformed

    private void tblChucVuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblChucVuMouseClicked
        // TODO add your handling code here:
        try {
            List<ChucVu> chucVus = cvSV.getList();
            int index = tblChucVu.getSelectedRow();
            ChucVu c = chucVus.get(index);
            lbId.setText(c.getId());
            txtMa.setText(c.getMa());
            txtTen.setText(c.getTen());
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_tblChucVuMouseClicked

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        try {
            ChucVu c = new ChucVu();
            c.setMa(txtMa.getText());
            c.setTen(txtTen.getText());

            cvSV.update(c, lbId.getText());
            loadTB();
            JOptionPane.showMessageDialog(this, "Sửa thành công");

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Sửa thất bại!");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        try {
            cvSV.delete(lbId.getText());
            loadTB();
            JOptionPane.showMessageDialog(this, "Xóa thành công");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Xóa thất bại!");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

