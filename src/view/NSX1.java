    private void tblNSXMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNSXMouseClicked
        // TODO add your handling code here:
        List<NSX> nsxs = nsxSV.getList();
        int index = tblNSX.getSelectedRow();
        NSX n = nsxs.get(index);
        lbID.setText(n.getId());
        txtMa.setText(n.getMa());
        txtTen.setText(n.getTen());
    }//GEN-LAST:event_tblNSXMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try {
            NSX nsx = new NSX();
            nsx.setMa(txtMa.getText());
            nsx.setTen(txtTen.getText());

            if (checkMa() == true) {
                nsxSV.add(nsx);
                loadTB();
                JOptionPane.showMessageDialog(this, "Thêm thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Mã đã tồn tại!");
                txtMa.setText("");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Thêm thất bại!");
        }

    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        try {
            int tl = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không?", "WARNING", JOptionPane.YES_NO_OPTION);
            if (tl == JOptionPane.YES_OPTION) {
                NSX nsx = new NSX();
                nsx.setMa(txtMa.getText());
                nsx.setTen(txtTen.getText());

                nsxSV.update(nsx, lbID.getText());
                loadTB();
                JOptionPane.showMessageDialog(this, "Sửa thành công");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Sửa thất bại!");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        try {
            int tl = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không?", "WARNING", JOptionPane.YES_NO_OPTION);
            if (tl == JOptionPane.YES_OPTION) {
                nsxSV.delete(lbID.getText());
                loadTB();
                JOptionPane.showMessageDialog(this, "Xóa thành công");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Xóa thất bại");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

