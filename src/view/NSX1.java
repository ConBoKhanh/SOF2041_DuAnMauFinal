    public boolean checkMa() {
        try {
            List<NSX> nsxs = nsxSV.getList();
            for (NSX n : nsxs) {
                if (txtMa.getText().equals(n.getMa())) {
                    return false;
                }
            }
        } catch (Exception e) {
        }
        return true;
    }

    public void loadTB() throws SQLException {
        List<NSX> nsxs = nsxSV.getList();
        model = (DefaultTableModel) tblNSX.getModel();
        model.setColumnCount(0);
        model.addColumn("Id");
        model.addColumn("Mã");
        model.addColumn("Tên");

        model.setRowCount(0);
        for (NSX n : nsxs) {
            model.addRow(new Object[]{
                n.getId(), n.getMa(), n.getTen()
            });
        }
    }

