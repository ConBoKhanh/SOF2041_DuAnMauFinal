    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try {
            SanPham sp = new SanPham();
            sp.setMaSP(txtMa.getText());
            sp.setTenSP(txtTen.getText());

            if (checkMa() == false) {
                JOptionPane.showMessageDialog(this, "Ma trung");
                return;
            }
            spSV.add(sp);
            loadTB();
            JOptionPane.showMessageDialog(this, "Them thanh cong");
            System.out.println(sp.toString());
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Them that bai!");
        }

    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        try {
            int tl = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không?", "WARNING", JOptionPane.YES_NO_OPTION);
            if (tl == JOptionPane.YES_OPTION) {
                SanPham sp = new SanPham();
                sp.setMaSP(txtMa.getText());
                sp.setTenSP(txtTen.getText());

                spSV.update(sp, lbID.getText());
                loadTB();
                System.out.println(spSV.getListSP().toString());
                JOptionPane.showMessageDialog(this, "Sửa thành công");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Sửa thất bại!");
        }

    }//GEN-LAST:event_btnSuaActionPerformed

