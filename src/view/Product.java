    public void loadTB() {
        List<SanPham> list = spSV.getListSP();
        model = (DefaultTableModel) tblSanPham.getModel();
        model.setColumnCount(0);
        model.addColumn("Id");
        model.addColumn("Ma");
        model.addColumn("Ten");

        model.setRowCount(0);
        for (SanPham s : list) {
            model.addRow(new Object[]{
                s.getId(), s.getMaSP(), s.getTenSP()
            });
        }
    }

    public boolean checkMa() {
        try {
            List<SanPham> sanPhams = spSV.getListSP();
            for (SanPham sp : sanPhams) {
                if (txtMa.getText().equals(sp.getMaSP())) {
                    return false;
                }
            }
        } catch (Exception e) {
        }
        return true;
    }

