    public void loadSanPham() {
        List<SanPhamBanHang> sanPhamBanHangs = spSV.getListSP();
        model = (DefaultTableModel) tblSanPham.getModel();
        model.setColumnCount(0);
        model.addColumn("Id");
        model.addColumn("Mã SP");
        model.addColumn("Tên SP");
        model.addColumn("Năm BH");
        model.addColumn("Mô Tả");
        model.addColumn("Số lượng");
        model.addColumn("Giá Nhập");
        model.addColumn("Giá Bán");

        model.setRowCount(0);
        for (SanPhamBanHang s : sanPhamBanHangs) {
            model.addRow(new Object[]{
                s.getId(), s.getMaSP(), s.getTenSP(), s.getNamBH(), s.getMoTa(), s.getSoLuong(), s.getGiaNhap(), s.getGiaBan()
            });
        }
    }

    public void loadHoaDon() {
        List<HoaDonViewModel> hoaDonViewModels = hdSV.getListHDBH();
        model = (DefaultTableModel) tblHoaDon.getModel();
        model.setColumnCount(0);
        model.addColumn("STT");
        model.addColumn("id");
        model.addColumn("Mã");
        model.addColumn("Ngày Tạo");
        model.addColumn("Tên Nhân viên");
        model.addColumn("Tình Trạng");

        model.setRowCount(0);
        for (HoaDonViewModel h : hoaDonViewModels) {
            model.addRow(new Object[]{
                model.getRowCount()+1,h.getId(), "Hóa Đơn " + h.getMa(), h.getNgayTao(), h.getTenNV(), h.getTrangThai() == 1 ? "Đã thanh toán" : "Chưa thanh toán"
            });
        }
    }
