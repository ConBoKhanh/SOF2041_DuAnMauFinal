    private void txtTienKhachDuaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTienKhachDuaKeyReleased
        try {
            int tienTong = Integer.parseInt(txtTongTien.getText());
            int tienTra = Integer.parseInt(txtTienKhachDua.getText());
            int tienthua = tienTong - tienTra;
            String a = String.valueOf(tienthua);
            String tientralai = a.replaceAll("[-]", "");// bo dau tru dau 
            txtTienThua.setText(tientralai + "  VND");
        } catch (Exception e) {
            System.out.println(e);
        }

    }//GEN-LAST:event_txtTienKhachDuaKeyReleased

    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        try {
            int index = tblSanPham.getSelectedRow();
            String idsp = tblSanPham.getValueAt(index, 0).toString();
            int giaban = Integer.parseInt(tblSanPham.getValueAt(index, 7).toString());
            String soLuongNhapinpit = JOptionPane.showInputDialog("Nhập Số Lượng Sản Phẩm " + " ");
            String idhd = txtIdHD.getText();
            int soluong = Integer.parseInt(soLuongNhapinpit);
            BigDecimal thanhtien = BigDecimal.valueOf(giaban * soluong);

            HoaDonChiTiet a = new HoaDonChiTiet();
            a.setIdChiTietSP(idsp);
            a.setIdHoaDon(txtIdHD.getText());
            a.setSoLuong(soluong);
            a.setDonGia(thanhtien);

            hdctSV.add(a);
            loadGioHang(txtIdHD.getText());
            int tongTien = hdctSV.getTongTien(txtIdHD.getText());
            txtTongTien.setText(String.valueOf(tongTien));

            System.out.println(a.toString());
        } catch (Exception e) {
            System.out.println(e);
        }

    }//GEN-LAST:event_tblSanPhamMouseClicked

    private void btnThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhToanActionPerformed
        try {
            int tiendua = Integer.parseInt(txtTienKhachDua.getText());
            int tientong = Integer.parseInt(txtTongTien.getText());
            if (tiendua < tientong) {
                JOptionPane.showMessageDialog(this, "thanh toán chưa đủ anh zai");
                return;
            }
            hdSV.updateThanhToan(txtIdHD.getText());
            loadHoaDon();
        } catch (NumberFormatException numberFormatException) {
            System.out.println(numberFormatException);
        } catch (HeadlessException headlessException) {
        }

    }//GEN-LAST:event_btnThanhToanActionPerformed

